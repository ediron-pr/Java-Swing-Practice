package my;

public class Student
{
	public String id ;
	public String name ;
	public boolean sex ;
	public String phone ;
	
	public Student()
	{
		
	}
	
	public Student(String id ,String name ,boolean sex ,String phone )
	{
		this.id = id;
		this.name = name;
		this.sex = sex;
		this.phone = phone;
	}
}
